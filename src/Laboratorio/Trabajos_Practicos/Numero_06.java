package Laboratorio.Trabajos_Practicos;

public class Numero_06 {

	public static void main(String[] args) {

		double A = 2.31;
		double B = 1.23;

		double seno = Math.sin(A);
		double coseno = Math.cos(A);
		double tangente = Math.tan(A);

		double atangente = Math.atan(A);
		double atangente2 = Math.atan2(A, B);

		double exponencial = Math.exp(B);
		double logaritmo = Math.log(B);

		double numeroPi = Math.PI;
		double numeroE = Math.E;


		System.out.println(seno);
		System.out.println(coseno);
		System.out.println(tangente);

		System.out.println(atangente);
		System.out.println(atangente2);

		System.out.println(exponencial);
		System.out.println(logaritmo);

		System.out.println(numeroPi);
		System.out.println(numeroE);
	}

}
